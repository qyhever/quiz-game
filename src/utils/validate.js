/**
 * 验证字符串
 * @param {String} value 源字符串
 * @param {String} type 验证类型
 * @return {Boolean} 验证结果
 */
export function validator(value, type) {
  const rules = {
    mobile(str) {
      return /^(0|86|17951)?(1[358][0-9]|14[579]|166|17[0135678])[0-9]{8}$/.test(str)
    },
    zh(str) {
      return /^[\u4E00-\u9FA5]+$/.test(str)
    },
    password(str) {
      return /^(\w|\?\=\.\*\[!@#\$%\^&\(\)\]){6,18}$/.test(str)
    },
    integer(str) {
      return /^[1-9][0-9]*$/.test(str)
    },
    // 两位小数
    numberTwoDecimal(str) {
      return /^(([1-9][0-9]*)|(([0]\.\d{1,2}|[1-9][0-9]*\.\d{1,2})))$/.test(str)
    },
    // 图形验证码
    imageVerifyCode(str) {
      return /^(\w|\d){4}$/.test(str)
    },
    // 手机验证码
    phoneVerifyCode(str) {
      return /^\d{4}$/.test(str)
    },
    email(str) {
      return /\w+([-+.]\w+)*@\w+([-.]\w+)*\.\w+([-.]\w+)*/.test(str)
    }
  }
  return rules[type] ? rules[type](value) : false
}

/**
 * @param {string} path
 * @returns {Boolean}
 */
export function isExternal(path) {
  return /^(https?:|mailto:|tel:)/.test(path)
}

/**
 * @param {string} url
 * @returns {Boolean}
 */
export function validURL(url) {
  const reg = /^(https?|ftp):\/\/([a-zA-Z0-9.-]+(:[a-zA-Z0-9.&%$-]+)*@)*((25[0-5]|2[0-4][0-9]|1[0-9]{2}|[1-9][0-9]?)(\.(25[0-5]|2[0-4][0-9]|1[0-9]{2}|[1-9]?[0-9])){3}|([a-zA-Z0-9-]+\.)*[a-zA-Z0-9-]+\.(com|edu|gov|int|mil|net|org|biz|arpa|info|name|pro|aero|coop|museum|[a-zA-Z]{2}))(:[0-9]+)*(\/($|[a-zA-Z0-9.,?'\\+&%$#=~_-]+))*$/
  return reg.test(url)
}

/**
 * @param {string} str
 * @returns {Boolean}
 */
export function validLowerCase(str) {
  const reg = /^[a-z]+$/
  return reg.test(str)
}

/**
 * @param {string} str
 * @returns {Boolean}
 */
export function validUpperCase(str) {
  const reg = /^[A-Z]+$/
  return reg.test(str)
}

/**
 * @param {string} str
 * @returns {Boolean}
 */
export function validAlphabets(str) {
  const reg = /^[A-Za-z]+$/
  return reg.test(str)
}

/**
 * @param {string} email
 * @returns {Boolean}
 */
export function validEmail(email) {
  const reg = /^(([^<>()\[\]\\.,;:\s@"]+(\.[^<>()\[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/
  return reg.test(email)
}
